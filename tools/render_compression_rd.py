#!/usr/bin/env python3
"""Render 6_dataset_compression rate-distortion figures + 50% table.

Reads results/training/6_dataset_compression/<dataset>/rd_curves.json and
headline_50pct.json; writes figures/rd_curves.{pdf,svg} and
tables/compression_50pct.tex in the same dataset dir.

Style per CLAUDE.md: Wong palette, one color + one line style per basis,
linear axes, no figure-level titles, PDF+SVG only.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt

BASE_DIR = Path("results/training/6_dataset_compression")
# (curve key candidates per dataset, color, linestyle, pretty label)
STYLES = [
    (("real_rich", "real_rich_8"), "#0072B2", "-",  "real rich (trained)"),
    (("rich", "rich_8"),           "#E69F00", "--", "rich (trained, complex)"),
    (("block_dct_8",),             "#009E73", "-.", "block DCT 8x8"),
]


def pareto(points):
    """Max-PSNR frontier: sort by bytes, keep points that improve PSNR."""
    pts = sorted(points, key=lambda p: p["bytes_per_image"])
    front, best = [], -1e9
    for p in pts:
        if p["test"]["mean_psnr"] > best:
            best = p["test"]["mean_psnr"]
            front.append(p)
    return front


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--dataset", required=True, choices=["quickdraw_5q", "div2k_8q"])
    args = ap.parse_args()

    ddir = BASE_DIR / args.dataset
    rd = json.loads((ddir / "rd_curves.json").read_text())
    hl = json.loads((ddir / "headline_50pct.json").read_text())
    meta, curves = rd["meta"], rd["curves"]
    n = meta["n_train"] + meta["n_test"]
    raw_bpi = meta["raw_bytes_per_image"]

    fig, ax = plt.subplots(figsize=(5.2, 3.6))
    for keys, color, ls, label in STYLES:
        key = next((k for k in keys if k in curves), None)
        if key is None:
            continue
        front = pareto(curves[key])
        ax.plot([p["bytes_per_image"] for p in front],
                [p["test"]["mean_psnr"] for p in front],
                color=color, linestyle=ls, marker="o", markersize=3,
                linewidth=1.6, label=label, zorder=3)
        best = hl["by_basis"].get(key)
        if best:
            ax.plot(best["bytes_per_image"], best["test"]["mean_psnr"],
                    marker="*", markersize=11, color=color, zorder=4,
                    markeredgecolor="black", markeredgewidth=0.5)

    ymin = ax.get_ylim()[0]
    ax.axvline(0.5 * raw_bpi, color="black", linestyle=":", linewidth=1.0, zorder=1)
    ax.annotate("50% of raw", xy=(0.5 * raw_bpi, ymin),
                xytext=(3, 4), textcoords="offset points", fontsize=8, rotation=90)
    for total, txt, cl in ((meta["deflate_raw_total"], "deflate (lossless)", "#555555"),
                           (meta["png_total"], "PNG (lossless)", "#999999")):
        ax.axvline(total / n, color=cl, linestyle=(0, (1, 3)), linewidth=1.0, zorder=1)
        ax.annotate(txt, xy=(total / n, ymin),
                    xytext=(3, 4), textcoords="offset points", fontsize=7,
                    rotation=90, color=cl)

    ax.set_xlabel("compressed size (bytes / image, incl. basis file)")
    ax.set_ylabel("test PSNR (dB)")
    ax.legend(fontsize=8, frameon=False, loc="upper left")
    ax.grid(alpha=0.25, linewidth=0.5)
    fig.tight_layout()

    figdir = ddir / "figures"
    figdir.mkdir(parents=True, exist_ok=True)
    fig.savefig(figdir / "rd_curves.pdf", bbox_inches="tight")
    fig.savefig(figdir / "rd_curves.svg", bbox_inches="tight")
    print(f"wrote {figdir}/rd_curves.{{pdf,svg}}")

    # ---- 50% table -------------------------------------------------------
    tabdir = ddir / "tables"
    tabdir.mkdir(parents=True, exist_ok=True)
    dct = hl["by_basis"].get("block_dct_8")
    rows = []
    for keys, _, _, label in STYLES:
        key = next((k for k in keys if k in hl["by_basis"]), None)
        p = hl["by_basis"].get(key) if key else None
        if p is None:
            continue
        gap = p["test"]["mean_psnr"] - dct["test"]["mean_psnr"] if dct else float("nan")
        rows.append(
            f"{label} & {p['bytes_per_image']:.0f} & {100*p['ratio_vs_raw']:.1f}\\% & "
            f"{p['test']['mean_psnr']:.2f} & {p['test']['mean_ssim']:.3f} & "
            f"{gap:+.2f} \\\\")
    tex = (
        "% Auto-generated by tools/render_compression_rd.py\n"
        "\\begin{tabular}{lrrrrr}\n\\toprule\n"
        "basis & B/img & \\% raw & PSNR (dB) & SSIM & $\\Delta$ vs DCT \\\\\n"
        "\\midrule\n" + "\n".join(rows) + "\n\\bottomrule\n\\end{tabular}\n")
    (tabdir / "compression_50pct.tex").write_text(tex)
    print(f"wrote {tabdir}/compression_50pct.tex")


if __name__ == "__main__":
    main()
