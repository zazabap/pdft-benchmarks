#!/usr/bin/env python3
"""Emit the seed-variance LaTeX table from seed_sweep.json.

One row per ordering: mean ± σ test PSNR at each keep ratio (n seeds), plus the
worst single seed (min) at ρ=.20 to show even the floor clears block-FFT. Two
classical reference rows (block-FFT 8×8 — the claim's bar — and block-DCT 8×8).
The per-ρ best learned ordering is bolded.

Writes <base>/tables/seed_variance.tex.

Usage:
    python tools/render_seed_variance_table.py \
        --base results/training/2_direct_training/random_seed/div2k_8q
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

RHOS = ["0.05", "0.1", "0.15", "0.2"]
LABEL = {"bg": "block-growth", "lr": r"left$\to$right", "rl": r"right$\to$left"}
DEFAULT_CLASSICAL = Path("results/training/2_direct_training/unfreeze/"
                         "reference/classical_div2k.json")


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--base", required=True)
    ap.add_argument("--classical", default=str(DEFAULT_CLASSICAL))
    args = ap.parse_args()

    base = Path(args.base)
    sweep = json.loads((base / "seed_sweep.json").read_text())
    per = sweep["per_ordering"]
    orderings = [o for o in ("bg", "lr", "rl") if o in per and per[o]["per_seed"]]
    if not orderings:
        print("[table] no populated orderings", file=sys.stderr)
        return 2
    classical = None
    if Path(args.classical).exists():
        classical = json.loads(Path(args.classical).read_text())

    # Per-ρ best learned mean (for bolding).
    best = {r: max(per[o]["agg"][r]["mean"] for o in orderings if r in per[o]["agg"])
            for r in RHOS}

    def fmt_cell(o, r):
        a = per[o]["agg"].get(r)
        if not a:
            return "--"
        s = f"{a['mean']:.2f}\\,$\\pm$\\,{a['std']:.2f}"
        if abs(a["mean"] - best[r]) < 1e-9:
            s = f"\\textbf{{{s}}}"
        return s

    n = per[orderings[0]]["agg"]["0.2"]["n"]
    lines = [
        r"% Auto-generated by tools/render_seed_variance_table.py — do not edit.",
        r"\begin{tabular}{l" + "r" * len(RHOS) + r"r}",
        r"\toprule",
        r"configuration & $\rho{=}.05$ & $\rho{=}.10$ & $\rho{=}.15$ & "
        r"$\rho{=}.20$ & min@$.20$ \\",
        r"\midrule",
    ]
    for o in orderings:
        cells = " & ".join(fmt_cell(o, r) for r in RHOS)
        mn = per[o]["agg"]["0.2"]["min"]
        lines.append(f"{LABEL.get(o, o)} & {cells} & {mn:.2f} \\\\")
    if classical:
        lines.append(r"\midrule")
        for key, lab in (("block_fft_8", "block-FFT 8×8"),
                         ("block_dct_8", "block-DCT 8×8")):
            ps = classical[key]["psnr"]
            cells = " & ".join(f"{ps[r]:.2f}" for r in RHOS)
            lines.append(f"{lab} & {cells} & -- \\\\")
    lines += [r"\bottomrule", r"\end{tabular}"]

    caption = (f"% n={n} random-init seeds per ordering; top-20\\% training "
               f"objective; mean$\\pm\\sigma$ test PSNR (dB) on the fixed "
               f"DIV2K test set.\n")
    out = base / "tables"
    out.mkdir(parents=True, exist_ok=True)
    (out / "seed_variance.tex").write_text(caption + "\n".join(lines) + "\n")
    print(f"[table] wrote {out/'seed_variance.tex'} (n={n}, orderings={orderings})")
    return 0


if __name__ == "__main__":
    sys.exit(main())
