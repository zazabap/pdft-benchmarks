#!/usr/bin/env python3
"""Generate the LaTeX paper table for DIV2K-8q PCA-vs-block-DCT.

Reads:
    <by-basis>/<basis>/metrics.json     # per trained basis
    <by-basis>/_baselines.json          # baseline numbers from cellify

Writes:
    --out (default: results/structure/div2k_8q_pca_vs_block_dct/tables/published_8q_div2k.tex)

Output format mirrors results/structure/quickdraw_pca_vs_block_dct/tables/
published_8q_quickdraw_v2.tex: a tabular block with rows grouped as
(classical-fixed / classical-fitted / learned-unblocked / learned-blocked)
and columns for keep ratios.
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

KEEP_RATIOS = (0.05, 0.10, 0.15, 0.20)

# Row order in the paper table. Must match the QuickDraw table's grouping.
ROWS = [
    # (label, key, group)
    ("FFT",                "fft",              "classical"),
    ("DCT",                "dct",              "classical"),
    ("DCT (rank)",         "dct_rank",         "classical"),
    ("Block-FFT 8",        "block_fft_8",      "classical"),
    ("Block-DCT 8",        "block_dct_8",      "classical"),
    ("Block-DCT 8 (rank)", "block_dct_8_rank", "classical"),
    ("BD-PCA",             "bd_pca",           "fitted"),
    ("Block BD-PCA 8",     "block_bd_pca_8",   "fitted"),
    ("Block-PCA 8 (rank)", "block_pca_8_rank", "fitted"),
    ("QFT",                "qft",              "learned"),
    ("Entangled QFT",      "entangled_qft",    "learned"),
    ("TEBD",               "tebd",             "learned"),
    ("MERA",               "mera",             "learned"),
    ("Blocked-8",          "blocked_8",        "learned-blocked"),
    ("Rich-8",             "rich_8",           "learned-blocked"),
    ("Real Rich-8",        "real_rich_8",      "learned-blocked"),
]


def fmt_psnr(v: float | None) -> str:
    if v is None:
        return "—"
    return f"{v:.2f}"


def get_psnr(entry: dict, kr: float) -> float | None:
    """Pull mean_psnr at keep ratio kr from a metrics-style entry."""
    if not entry or "metrics" not in entry:
        return None
    m = entry["metrics"]
    # Keys may be "0.05" or "0.1" (Python json normalizes 0.10 → 0.1).
    for k in (f"{kr}", f"{kr:.2f}", f"{kr:.1f}"):
        if k in m:
            return m[k].get("mean_psnr")
    return None


def render(by_basis: Path) -> str:
    # Load baselines
    baselines_path = by_basis / "_baselines.json"
    baselines = json.loads(baselines_path.read_text()) if baselines_path.exists() else {}

    # Load each trained basis cell
    trained: dict[str, dict] = {}
    for basis_dir in sorted(by_basis.iterdir()):
        if not basis_dir.is_dir():
            continue
        cell_metrics = basis_dir / "metrics.json"
        if not cell_metrics.exists():
            continue
        cell = json.loads(cell_metrics.read_text())
        # Each cell has exactly one top-level key — the basis name.
        for k, v in cell.items():
            trained[k] = v

    # Build the LaTeX
    lines = [
        "% Trimmed headline rate-distortion table for the paper.",
        "% Rows: 6 classical-fixed (incl. rank-truncation variants) + 3 classical-fitted",
        "%       (BD-PCA + Block BD-PCA 8 + Block-PCA 8 rank) + 4 learned-unblocked",
        "%       + 3 learned-blocked",
        "% Generated by tools/paper/render_div2k_paper_table.py from",
        "% results/structure/div2k_8q_pca_vs_block_dct/by_basis/.",
        "% PSNR (dB), mean over n_test=50 DIV2K test images.",
        "%",
        "\\begin{tabular}{l" + "c" * len(KEEP_RATIOS) + "}",
        "\\toprule",
        "Method & " + " & ".join(f"$\\rho={kr}$" for kr in KEEP_RATIOS) + " \\\\",
        "\\midrule",
    ]
    last_group = None
    for label, key, group in ROWS:
        if last_group is not None and group != last_group:
            lines.append("\\midrule")
        last_group = group
        entry = baselines.get(key) or trained.get(key)
        psnrs = [fmt_psnr(get_psnr(entry, kr)) for kr in KEEP_RATIOS]
        lines.append(f"{label} & " + " & ".join(psnrs) + " \\\\")
    lines.append("\\bottomrule")
    lines.append("\\end{tabular}")
    return "\n".join(lines) + "\n"


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--by-basis", type=Path,
                    default=Path("results/structure/div2k_8q_pca_vs_block_dct/by_basis"))
    ap.add_argument("--out", type=Path,
                    default=Path("results/structure/div2k_8q_pca_vs_block_dct/tables/published_8q_div2k.tex"))
    args = ap.parse_args()

    if not args.by_basis.is_dir():
        print(f"[paper-table] FAIL: --by-basis {args.by_basis} is not a directory",
              file=sys.stderr)
        return 1

    text = render(args.by_basis)
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(text)
    print(f"[paper-table] wrote {args.out} ({len(text)} bytes)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
